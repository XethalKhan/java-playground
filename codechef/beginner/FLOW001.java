package codechef.beginner;

public class FLOW001 {

		public static void main(String[] args) {
			System.out.println("1 + 2 = " + sum(1, 2));
			System.out.println("100 + 200 = " + sum(100, 200));
			System.out.println("10 + 40 = " + sum(10, 40));
		}

		public static int sum(int a, int b) {
			return a + b;
		}
}
