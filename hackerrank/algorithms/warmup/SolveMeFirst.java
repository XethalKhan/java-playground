package hackerrank.algorithms.warmup;

public class SolveMeFirst {

	public static void main(String[] args) {
		int a = 10;
		int b = 37;
		System.out.println(solveMeFirst(a, b));
	}

	public static int solveMeFirst(int a, int b) {
		return a + b;
	}
}
